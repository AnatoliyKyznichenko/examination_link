async def get_balance():
    async with Client("my_session", api_id, api_hash) as app:
        async for message in app.get_chat_history(chat_id=""):
            id_balances = str(message.text).split(':')[-1]
            if len(message.text.split(':')) != 3:
                return None
            return id_balances


